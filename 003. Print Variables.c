#include <stdio.h>

int main() {
    int number = 25;
    printf("The number is %d \n", number); // Output: The number is 25
    
    float pi = 3.14;
    printf("The pi is %f \n", pi); // Output: The pi is 3.140000
    
    char star = '*';
    printf("Star is %c \n", star); // Output: Star is *
    
    return 0;
}
